function get_total_slot_remain($remainSlot, $slotId, $arrBookedservices){
    // dd($remainSlot, $slotId, $arrBookedservices);

    foreach ($arrBookedservices as $arrb) {
        if ($arrb->doing_time_id == $slotId) {
            $remainSlot -= 1;
        }
    }
    return $remainSlot;
}
function truncate($string, $length, $subfix = '...')
    {
        $stringLength = strlen($string);
        if ($length < $stringLength) {
            $space = strpos($string, ' ', $length);
            $tab = strpos($string, "\t", $length);
            $lineFeed = strpos($string, "\n", $length);

            if ($space === false) {
                $space = 1000000;
            }

            if ($tab === false) {
                $tab = 1000000;
            }

            if ($lineFeed === false) {
                $lineFeed = 1000000;
            }

            $length = min($space, $tab, $lineFeed);

            if ($length < $stringLength) {
                $string = substr($string, 0, $length) . $subfix;
            }
        }

        //Trả kết quả
        return $string;
    }
function create_slug($string)
{
    $search = array(
        '#(à|á|ạ|ả|ã|â|ầ|ấ|ậ|ẩ|ẫ|ă|ằ|ắ|ặ|ẳ|ẵ)#',
        '#(è|é|ẹ|ẻ|ẽ|ê|ề|ế|ệ|ể|ễ)#',
        '#(ì|í|ị|ỉ|ĩ)#',
        '#(ò|ó|ọ|ỏ|õ|ô|ồ|ố|ộ|ổ|ỗ|ơ|ờ|ớ|ợ|ở|ỡ)#',
        '#(ù|ú|ụ|ủ|ũ|ư|ừ|ứ|ự|ử|ữ)#',
        '#(ỳ|ý|ỵ|ỷ|ỹ)#',
        '#(đ)#',
        '#(À|Á|Ạ|Ả|Ã|Â|Ầ|Ấ|Ậ|Ẩ|Ẫ|Ă|Ằ|Ắ|Ặ|Ẳ|Ẵ)#',
        '#(È|É|Ẹ|Ẻ|Ẽ|Ê|Ề|Ế|Ệ|Ể|Ễ)#',
        '#(Ì|Í|Ị|Ỉ|Ĩ)#',
        '#(Ò|Ó|Ọ|Ỏ|Õ|Ô|Ồ|Ố|Ộ|Ổ|Ỗ|Ơ|Ờ|Ớ|Ợ|Ở|Ỡ)#',
        '#(Ù|Ú|Ụ|Ủ|Ũ|Ư|Ừ|Ứ|Ự|Ử|Ữ)#',
        '#(Ỳ|Ý|Ỵ|Ỷ|Ỹ)#',
        '#(Đ)#',
        "/[^a-zA-Z0-9\-\_]/",
    );
    $replace = array(
        'a',
        'e',
        'i',
        'o',
        'u',
        'y',
        'd',
        'A',
        'E',
        'I',
        'O',
        'U',
        'Y',
        'D',
        '-',
    );
    $string = preg_replace($search, $replace, $string);
    $string = preg_replace('/(-)+/', '-', $string);
    $string = strtolower($string);
    return $string;
}

